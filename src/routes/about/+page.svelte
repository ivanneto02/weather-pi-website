<h1>About the WeatherPI Data Viewer</h1>

<p>This website is used to visualize the data gathered by BME280 3.3V High Precision (pressure, temperature, and humidity sensor), and PMS5003 Digital Particle Concentration Laser Sensor.</p>

<p>These sensors gather the following data from outside my apartment:</p>

<ul>
    <li>Temperature</li>
    <li>Humidity</li>
    <li>Atmospheric Pressure</li>
</ul>
